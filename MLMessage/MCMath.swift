import UIKit

public class MCMath: NSObject {

    public func sum(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func min(a: Int, b: Int) -> Int {
        return a - b
    }
    
    public func appendString(str1: String, str2: String) -> Int {
        return str1 + str2
    }
}
